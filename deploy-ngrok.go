// Deploy orchestrates the deployment process
func Deploy(ctx context.Context) error {
	log.Println("=== Starting Ngrok Deployment ===")

	// Initialize configurator
	configurator := NewConfigurator()
	if err := configurator.LoadEnv(); err != nil {
		log.Printf("Warning: could not load .env file: %v", err)
	}

	// Create wait group for concurrent operations
	var wg sync.WaitGroup
	errChan := make(chan error, 2)

	// Start ngrok tunnel
	ngrokManager := NewNgrokManager(ctx)
	wg.Add(1)
	go func() {
		defer wg.Done()
		if err := ngrokManager.Start(3000); err != nil {
			errChan <- fmt.Errorf("ngrok error: %w", err)
		}
	}()

	// Start website
	websiteManager := NewWebsiteManager(configurator)
	wg.Add(1)
	go func() {
		defer wg.Done()
		if err := websiteManager.Start(ctx); err != nil {
			errChan <- fmt.Errorf("website error: %w", err)
		}
	}()

	// Wait for initial startup
	time.Sleep(3 * time.Second)

	// Get ngrok URL and update configuration
	go func() {
		ngrokURL := ngrokManager.GetURL()
		log.Printf("[config] Updating .env with ngrok URL: %s", ngrokURL)

		if err := configurator.UpdateDomain(ngrokURL); err != nil {
			log.Printf("Error updating domain: %v", err)
			return
		}

		if err := configurator.SaveEnv(); err != nil {
			log.Printf("Error saving .env: %v", err)
			return
		}

		log.Println("[config] .env file updated successfully")
	}()

	// Wait for errors or context cancellation
	go func() {
		wg.Wait()
		close(errChan)
	}()

	select {
	case err := <-errChan:
		return err
	case <-ctx.Done():
		log.Println("Deployment stopped by user")
		return nil
	}
}

func main() {
	// Initialize paths based on current working directory
	initPaths()

	ctx, cancel := context.WithCancel(context.Background())
	defer cancel()

	// Handle interrupt signal
	sigChan := make(chan os.Signal, 1)
	// signal.Notify(sigChan, os.Interrupt, syscall.SIGTERM)

	go func() {
		<-sigChan
		log.Println("\nReceived interrupt signal, shutting down...")
		cancel()
	}()

	if err := Deploy(ctx); err != nil {
		log.Fatalf("Deployment failed: %v", err)
	}
}
